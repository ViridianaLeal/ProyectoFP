package modelo;

public class Grupo {
	int idGrupo;
	int Grupo;
	
	public Grupo() {
		
	}

	public int getIdGrupo() {
		return idGrupo;
	}

	public void setIdGrupo(int idGrupo) {
		this.idGrupo = idGrupo;
	}

	public int getGrupo() {
		return Grupo;
	}

	public void setGrupo(int grupo) {
		Grupo = grupo;
	}

}
